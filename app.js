// Fonction pour parser les horaires et déterminer si un événement est en cours
function parseScheduleAndCheck(horaire) {
  const now = new Date();
  const nowDay = now.toLocaleDateString('fr-FR', {weekday: 'long'}).toLowerCase();
  const nowHour = now.getHours();
  const nowMinute = now.getMinutes();
  const nowTime = nowHour * 60 + nowMinute; // Minutes depuis minuit

  // Mapping des jours français
  const dayMapping = {
    'samedi': 'samedi',
    'dimanche': 'dimanche', 
    'lundi': 'lundi',
    'mardi': 'mardi',
    'mercredi': 'mercredi',
    'jeudi': 'jeudi',
    'vendredi': 'vendredi'
  };

  let [day, timeLabel] = horaire.split(' ');
  day = day.toLowerCase();

  // Vérifier si on est le bon jour
  if (!dayMapping[day] || dayMapping[day] !== nowDay) {
    return false;
  }

  let eventTime = 0;
  let tolerance = 30; // 30 minutes de tolérance par défaut

  // Parser différents formats d'heures
  if (timeLabel === 'midi') {
    eventTime = 12 * 60; // 12h00 en minutes
    tolerance = 120; // 2h de tolérance pour midi
  } else if (timeLabel === 'soir' || timeLabel === 'soirée') {
    eventTime = 19 * 60; // 19h00 en minutes
    tolerance = 120; // 2h de tolérance pour le soir
  } else if (timeLabel && timeLabel.includes('h')) {
    // Format "20h30" ou "9h15"
    let [h, min] = timeLabel.split('h');
    h = parseInt(h);
    min = parseInt(min || "0");
    eventTime = h * 60 + min;
    tolerance = 30; // 30 minutes de tolérance
  } else {
    return false; // Format non reconnu
  }

  // Vérifier si on est dans la fenêtre de tolérance
  const timeDiff = Math.abs(nowTime - eventTime);
  return timeDiff <= tolerance;
}

// Fonction pour valider automatiquement les événements en cours
async function autoValidateCurrentEvents(participantId) {
    const participant = participantsDatabase[participantId];
    if (!participant) return;
    
    let validatedCount = 0;
    for (let i = 0; i < participant.evenements.length; i++) {
        const event = participant.evenements[i];
        // Vérifier si l'événement est en cours
        if (parseScheduleAndCheck(event.horaire)) {
            try {
                const key = `${participantId}-${i}`;
                const currentStatus = validationsData[key] || 'none';
                if (currentStatus === 'none') {
                    await updateEventValidation(participantId, i, 'validated');
                    validatedCount++;
                    console.log(`Auto-validation: ${event.nom} - ${event.horaire}`);
                }
            } catch (error) {
                console.error('Erreur lors de l\'auto-validation:', error);
            }
        }
    }
    
    // Compter le nombre total de places validées après l'auto-validation
    const totalValidated = countValidatedEvents(participantId);
    
    if (validatedCount > 0) {
        showScanResult(`✓ ${participant.nom} - ${validatedCount} événement(s) validé(s) - Total: ${totalValidated} place(s)`, 'success');
    } else {
        showScanResult(`${participant.nom} - Aucun événement en cours actuellement - Total validé: ${totalValidated} place(s)`, 'info');
    }
}


