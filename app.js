// Scanner QR
function startQRScanner() {
  const qrReaderElement = document.getElementById('qr-reader');

  if (!html5QrCode) {
    html5QrCode = new Html5Qrcode("qr-reader");
  }

  const config = {
    fps: 10,
    qrbox: { width: 250, height: 250 },
    aspectRatio: 1.0
  };

  html5QrCode.start(
    { facingMode: "environment" },
    config,
    (decodedText) => {
      handleQRCodeResult(decodedText);
    },
    (errorMessage) => {
      // Ignorer les erreurs de scan normales
    }
  ).then(() => {
    document.getElementById('startScan').classList.add('hidden');
    document.getElementById('stopScan').classList.remove('hidden');
  }).catch(err => {
    console.error('Erreur démarrage scanner:', err);
    showScanResult('Erreur: Impossible d\'accéder à la caméra', 'error');
  });
}

function stopQRScanner() {
  if (html5QrCode) {
    html5QrCode.stop().then(() => {
      document.getElementById('startScan').classList.remove('hidden');
      document.getElementById('stopScan').classList.add('hidden');
    }).catch(err => {
      console.error('Erreur arrêt scanner:', err);
    });
  }
}

function handleFileUpload(event) {
  const file = event.target.files[0];
  if (file) {
    if (!html5QrCode) {
      html5QrCode = new Html5Qrcode("qr-reader");
    }

    html5QrCode.scanFile(file, true)
      .then(decodedText => {
        handleQRCodeResult(decodedText);
      })
      .catch(err => {
        showScanResult('Erreur: Aucun QR code détecté dans l\'image', 'error');
      });
  }
}

function handleQRCodeResult(decodedText) {
    const participant = participantsDatabase[decodedText];
    
    if (!participant) {
        // Participant non trouvé - fond rouge
        setBackgroundFlash('error');
        showScanResult(`QR Code non valide: ${decodedText}`, 'error');
        return;
    }
    
    // Vérifier si TOUS les événements sont déjà validés
    const allValidated = areAllEventsValidated(decodedText);
    
    if (allValidated) {
        // Tous les événements déjà validés - fond rouge
        setBackgroundFlash('error');
        const validatedCount = countValidatedEvents(decodedText);
        showScanResult(`⚠️ ${participant.nom} - Déjà validé (${validatedCount} place(s))`, 'error');
        displayParticipant(decodedText);
        return;
    }
    
    // Participant trouvé avec événements à valider - fond vert
    setBackgroundFlash('success');
    
    // Afficher le participant
    displayParticipant(decodedText);
    
    // Auto-validation des événements en cours
    autoValidateCurrentEvents(decodedText);
}

// Nouvelle fonction pour vérifier si tous les événements sont déjà validés
function areAllEventsValidated(participantId) {
    const participant = participantsDatabase[participantId];
    if (!participant) return false;
    
    for (let i = 0; i < participant.evenements.length; i++) {
        const key = `${participantId}-${i}`;
        const status = validationsData[key] || 'none';
        if (status !== 'validated') {
            return false; // Au moins un événement n'est pas validé
        }
    }
    return true; // Tous les événements sont validés
}

// Nouvelle fonction pour compter les événements validés
function countValidatedEvents(participantId) {
    const participant = participantsDatabase[participantId];
    if (!participant) return 0;
    
    let count = 0;
    for (let i = 0; i < participant.evenements.length; i++) {
        const key = `${participantId}-${i}`;
        const status = validationsData[key] || 'none';
        if (status === 'validated') {
            count++;
        }
    }
    return count;
}

// Nouvelle fonction pour le flash de fond d'écran
function setBackgroundFlash(type) {
    const body = document.body;
    const originalBackground = body.style.backgroundColor;
    
    // Définir la couleur selon le type
    if (type === 'success') {
        body.style.backgroundColor = '#4CAF50'; // Vert
    } else if (type === 'error') {
        body.style.backgroundColor = '#f44336'; // Rouge
    }
    
    // Créer un overlay pour un meilleur effet visuel
    const overlay = document.createElement('div');
    overlay.style.position = 'fixed';
    overlay.style.top = '0';
    overlay.style.left = '0';
    overlay.style.width = '100%';
    overlay.style.height = '100%';
    overlay.style.backgroundColor = type === 'success' ? 'rgba(76, 175, 80, 0.8)' : 'rgba(244, 67, 54, 0.8)';
    overlay.style.zIndex = '9999';
    overlay.style.pointerEvents = 'none';
    overlay.style.transition = 'opacity 0.3s ease-out';
    document.body.appendChild(overlay);
    
    // Retirer l'effet après 1 seconde
    setTimeout(() => {
        overlay.style.opacity = '0';
        setTimeout(() => {
            body.style.backgroundColor = originalBackground;
            document.body.removeChild(overlay);
        }, 300);
    }, 1000);
}


